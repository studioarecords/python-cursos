# Operadores de asignacion

edad = 55
print(edad)

edad += 5
print(edad)

edad -= 5
print(edad)

edad *= 5
print(edad)

edad /= 5
print(int(edad))

# Operadores de omcremento y decremento
year = 2021


# Pre incremento
year = 1 + year

# Pre decremento
year = 1 - year

print(year)
